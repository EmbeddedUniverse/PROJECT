clear all
close all
clc

%% CONTROL ON PERCENTAGE RESTRICTION
PERCENTAGE_OF_STD = 1; %IDEALLY DO NOT PUT A NUMBER BELOW 50%, BECAUSE ITS TOO SEVERE...

%% CONSTANTS
%MEANS
MEAN_P_0_1000=36.936;%
MEAN_P_1000_5000=31.182;%
MEAN_P_5000_8000=31.882;%

MEAN_I_0_1000=46.5856;%
MEAN_I_1000_5000=39.3716;%
MEAN_I_5000_8000=14.0428;%

MEAN_U_0_1000=59.9853;%
MEAN_U_1000_5000=35.1901;%
MEAN_U_5000_8000=4.8246;%

%STD
STD_P_0_1000=14.327;%
STD_P_1000_5000=7.857;%
STD_P_5000_8000=14.354;%

STD_I_0_1000=11.1922;%
STD_I_1000_5000=9.2249;%
STD_I_5000_8000=6.2643;%

STD_U_0_1000=10.3329;%
STD_U_1000_5000=8.7398;%
STD_U_5000_8000=2.1936;%

%% VARIABLES
%MIN AND MAX
MIN_P_0_1000=(MEAN_P_0_1000-STD_P_0_1000*PERCENTAGE_OF_STD)/100;%
MIN_P_1000_5000=(MEAN_P_1000_5000-STD_P_1000_5000*PERCENTAGE_OF_STD)/100;%
MIN_P_5000_8000=(MEAN_P_5000_8000-STD_P_5000_8000*PERCENTAGE_OF_STD)/100;%

MAX_P_0_1000=(MEAN_P_0_1000+STD_P_0_1000*PERCENTAGE_OF_STD)/100;%
MAX_P_1000_5000=(MEAN_P_1000_5000+STD_P_1000_5000*PERCENTAGE_OF_STD)/100;%
MAX_P_5000_8000=(MEAN_P_5000_8000+STD_P_5000_8000*PERCENTAGE_OF_STD)/100;%

MIN_I_0_1000=(MEAN_I_0_1000-STD_I_0_1000*PERCENTAGE_OF_STD)/100;%
MIN_I_1000_5000=(MEAN_I_1000_5000-STD_I_1000_5000*PERCENTAGE_OF_STD)/100;%
MIN_I_5000_8000=(MEAN_I_5000_8000-STD_I_5000_8000*PERCENTAGE_OF_STD)/100;%

MAX_I_0_1000=(MEAN_I_0_1000+STD_I_0_1000*PERCENTAGE_OF_STD)/100;%
MAX_I_1000_5000=(MEAN_I_1000_5000+STD_I_1000_5000*PERCENTAGE_OF_STD)/100;%
MAX_I_5000_8000=(MEAN_I_5000_8000+STD_I_5000_8000*PERCENTAGE_OF_STD)/100;%

MIN_U_0_1000=(MEAN_U_0_1000-STD_U_0_1000*PERCENTAGE_OF_STD)/100;%
MIN_U_1000_5000=(MEAN_U_1000_5000-STD_U_1000_5000*PERCENTAGE_OF_STD)/100;%
MIN_U_5000_8000=(MEAN_U_5000_8000-STD_U_5000_8000*PERCENTAGE_OF_STD)/100;%

MAX_U_0_1000=(MEAN_U_0_1000+STD_U_0_1000*PERCENTAGE_OF_STD)/100;%
MAX_U_1000_5000=(MEAN_U_1000_5000+STD_U_1000_5000*PERCENTAGE_OF_STD)/100;%
MAX_U_5000_8000=(MEAN_U_5000_8000+STD_U_5000_8000*PERCENTAGE_OF_STD)/100;%

figure()
plot(1:1000,ones(1000,1)*MIN_P_0_1000,'r',...
    1:1000,ones(1000,1)*MAX_P_0_1000,'r',...
    1:1000,ones(1000,1)*MIN_I_0_1000,'b',...
    1:1000,ones(1000,1)*MAX_I_0_1000,'b',...
    1:1000,ones(1000,1)*MIN_U_0_1000,'g',...
    1:1000,ones(1000,1)*MAX_U_0_1000,'g')
title('0 to 1000Hz percent range comparison')
ylabel('% of energy proportion')
legend('P','','I','','U','')

figure()
plot(1:1000,ones(1000,1)*MIN_P_1000_5000,'r',...
    1:1000,ones(1000,1)*MAX_P_1000_5000,'r',...
    1:1000,ones(1000,1)*MIN_I_1000_5000,'b',...
    1:1000,ones(1000,1)*MAX_I_1000_5000,'b',...
    1:1000,ones(1000,1)*MIN_U_1000_5000,'g',...
    1:1000,ones(1000,1)*MAX_U_1000_5000,'g')
title('1000 to 5000Hz percent range comparison')
ylabel('% of energy proportion')
legend('P','','I','','U','')

figure()
plot(1:1000,ones(1000,1)*MIN_P_5000_8000,'r',...
    1:1000,ones(1000,1)*MAX_P_5000_8000,'r',...
    1:1000,ones(1000,1)*MIN_I_5000_8000,'b',...
    1:1000,ones(1000,1)*MAX_I_5000_8000,'b',...
    1:1000,ones(1000,1)*MIN_U_5000_8000,'g',...
    1:1000,ones(1000,1)*MAX_U_5000_8000,'g')
title('5000 to 8000Hz percent range comparison')
ylabel('% of energy proportion')
legend('P','','I','','U','')
